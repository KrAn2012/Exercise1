public class Calculation {

    double add(double firstValue, double nextValue) {
        return firstValue + nextValue;
    }

    double subtraction(double firstValue, double nextValue) {
        return firstValue - nextValue;
    }

    double multiplication(double firstValue, double nextValue) {
        return firstValue * nextValue;
    }

    double division(double firstValue, double nextValue) {
        return firstValue / nextValue;
    }

    double modulo(double firstValue, double nextValue) {
        return firstValue % nextValue;
    }
}


